export function Satisfy(x,y){
    if(x<0 || x>782 || y<0 || y>576){
        return true;
    }
    for(let i=0;i<collisionArea.length;i++){
        const obj=collisionArea[i];
        if(x<obj.x+obj.w && x+14>obj.x){
            if(y<obj.y+obj.h && y+19>obj.y){
                return true;    
            }
        } 
    }
    return false;
}